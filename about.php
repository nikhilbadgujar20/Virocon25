													The Indian Virological Society (IVS), a member of International Union of Microbiological Society (IUMS), was established in December 1984 at Haryana Agriculture University, Hisar, with the objective to promote research and development in the ﬁeld of virology. It is a unique scientific body that provides platform for those associated with the characterization and management of viruses affecting Animal, Human, Fish, Insect, Plant and other living organisms. The Society also organizes National and International Conferences, Trainings, Seminars, Workshops, Invited lectures, Academia-Industry-meet, etc. The Society also recognizes the outstanding contributions of the individual members by electing them as Fellows of the society and conferring lifetime achievement awards, Springer Nature-IVS Best Publication Award and Young Scientist awards. The society has close collaboration with the World Society for Virology which is emerging as a leading world forum for addressing problem related to cross boundary viral epidemics.</p>
